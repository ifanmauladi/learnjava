public class Main{
  public static void main(String[] args){
    System.out.println("we will learn operator and datatype");
    //Byte data type
    byte valueByte = 100;
    //arithmetic operator
    System.out.println("1. arithmetic in byte");

    System.out.println("- addition operator");
    valueByte = (byte)(valueByte+2);
    System.out.println(valueByte);

    System.out.println("- substraction operator");
    valueByte = (byte)(valueByte-7);
    System.out.println(valueByte);
  }
}
